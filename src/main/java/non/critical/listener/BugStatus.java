package non.critical.listener;

/**
 * This enum represents the possible status values that a bug can have in the TicketSystem.
 * These values include ACTIVE, CLOSED, RESOLVED, NEW, DONE, TO_DO, and IN_PROGRESS.
 * Users can modify this enum to include additional status values or modify existing ones, depending on the needs of their system.
 */
public enum BugStatus {
    ACTIVE, CLOSED, RESOLVED, NEW, DONE, TO_DO, IN_PROGRESS
}
